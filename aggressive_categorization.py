def categorize_aggressive(profession_name: str) -> tuple[str, str]:
    """Агрессивная категоризация профессии."""
    name_lower = profession_name.lower()
    
    # Проверяем по порядку приоритета
    for domain in DOMAIN_PRIORITY:
        if domain in AGGRESSIVE_RULES:
            keywords = AGGRESSIVE_RULES[domain]
            if any(kw in name_lower for kw in keywords):
                # Возвращаем первый подходящий домен
                return domain, get_sphere_for_domain(domain, profession_name)
    
    # Если ничего не подошло - OTHER
    return "OTHER", "Другое"

